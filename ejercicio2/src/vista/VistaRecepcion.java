    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        double capturarPeso;
        double precioEnCOP;
        double precioEnUSD;
        Object object[] = null;
        DefaultTableModel modelo;
        capturarPeso = Double.parseDouble(txtPesoBulto.getText());
        
        if (objAvion.validarAgregar(capturarPeso)) {
            precioEnCOP = objProcesos.calcularPrecioCop(capturarPeso);
            precioEnUSD = objProcesos.convertirADolar(precioEnCOP);
            objAvion.agregarBulto(capturarPeso, precioEnCOP, precioEnUSD);
            modelo = (DefaultTableModel) tblInformacion.getModel();
            modelo.addRow(object);
        } else {
            JOptionPane.showMessageDialog(null, "Bulto supera el peso maximo");
        }
        
        //mostrar bultos en tabla
        for (int i = 0; i < objAvion.objAvion.getBultos().size(); i++) {
            Bulto objBulto = this.objAvion.objAvion.getBultos().get(i);
            tblInformacion.setValueAt(objBulto.getPeso(), i, 0);
            tblInformacion.setValueAt(objBulto.getPrecioEnCop(), i, 1);
            tblInformacion.setValueAt(objBulto.getPrecioEnDolar(), i, 2);
        }
        txtPesoBulto.setText(null);
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        if (this.objAvion.objAvion.getContador()>=15) {
            this.setVisible(false);
            double pesoCarga = this.objAvion.objAvion.getPesoAcumulado();
            int numeroDeBultos = this.objAvion.objAvion.getContador();
            double masPesado = this.objProcesos.masPesado(this.objAvion.objAvion.getBultos());
            double masLiviano = this.objProcesos.menosPesado(this.objAvion.objAvion.getBultos());
            double promedio = this.objProcesos.calcularPromedio(numeroDeBultos, pesoCarga);
            double precioEnCOP = this.objProcesos.valorTotalCop(this.objAvion.objAvion.getBultos());
            double precioEnUSD = this.objProcesos.valorTotalUsd(this.objAvion.objAvion.getBultos());
            
            VistaResultado objVResultado = new VistaResultado();
            objVResultado.tblResultado.setValueAt(numeroDeBultos, 0, 0);
            objVResultado.tblResultado.setValueAt(masPesado, 0, 1);
            objVResultado.tblResultado.setValueAt(masLiviano, 0, 2);
            objVResultado.tblResultado.setValueAt(promedio, 0, 3);
            objVResultado.tblResultado.setValueAt(precioEnCOP, 0, 4);
            objVResultado.tblResultado.setValueAt(precioEnUSD, 0, 5);
            objVResultado.setVisible(true);
            
        }else{
           JOptionPane.showMessageDialog(null, "AÃºn no alcanza el numero de bultos minimos (15)");
        }
        
    }//GEN-LAST:event_btnFinalizarActionPerformed

